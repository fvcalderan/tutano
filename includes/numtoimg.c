#include "numtoimg.h"

int num_to_img(int num, int pos)
{
    int i, rem, len = 0, n;
 
    n = num;
    while (n != 0)
    {
        len++;
        n /= 10;
    }

    for (i = 0; i < len; i++)
    {
        rem = num % 10;
        num = num / 10;
        if (i == pos)
            return rem + 285;
    }
    return 285;
}
